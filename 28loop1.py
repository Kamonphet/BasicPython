'''
while expression :     สร้างเงื่อนไขที่ต้องเป็นจริงถึงจะมีการดำเนินการต่อ  เมื่อเงื่อนไขเป็นเท็จแล้วจะทำให้หลุดจาก while
ระวังการใส่เงื่อนไขถ้าไม่มีการออกจากลูปจะทำให้โปรแกรมทำงานไปเรื่อย ๆอาจทำให้โปรแกรมแฮงก์ได้
    statement
'''
i=1 # ตัวนับจำนวน

while i<=10:
    print("รอบที่ :",i)
    i=i+1
print("จบโปรแกรม")