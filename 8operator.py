#ตัวดำเนินการทางคณิตศาสตร์ (operator)

#ตัวดำเนินการ & ตัวที่ถูกดำเนินการ
#Operator | Operand

#A + B
# A , B => Operand
# + => Operator
x = 5
y = 10

print("ผลบวก", x+y)
print("ผลลบ", x-y)
print("ผลคูณ", x*y)
print("ผลหาร", x/y)
print("ผลหารปัดเศษ", x//y)
print("ผลหารเอาเศษ", x % y)
print("ยกกำลัง", x**y)
